package com.javafundamental.operator;

public class OperatorAritmatika {
    public static void main(String[] args) {
        System.out.println("Jumlah");
        int hasilPenambahan = 5 + 5;
        System.out.println("Hasilpenambahan diatas adalah " + hasilPenambahan);

        int hasilPengurangan = 10 - 5;
        System.out.println("Hasil pengurangan diatas adalah " + hasilPengurangan);

        int hasilPerkalian = 5 * 5;
        System.out.println("Hasil perkalian diatas adalah " + hasilPerkalian);

        int hasilPembagian = 100 / 5;
        System.out.println("Hasil pembagian diatas adalah " + hasilPembagian);

        int hasilSisaBagi = 99 % 2;
        System.out.println("Sisa hasil bagi " + hasilSisaBagi);

    }
}
